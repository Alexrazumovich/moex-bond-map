# ─── Поиск по MOEX ISS ───────────────────────────────────────────────────────
$newRatings = @{}
$i = 0

foreach ($entry in $issuerMap.GetEnumerator()) {
    $i++
    $query   = [Uri]::EscapeUriString($entry.Key)
    $rating  = $entry.Value
    $hits    = 0

    try {
        $url  = "https://iss.moex.com/iss/securities.json?q=$query&is_trading=1&group_by=group&group_by_filter=stock_bonds&iss.meta=off&securities.columns=SECID,ISIN,SHORTNAME"
        $resp = Invoke-RestMethod $url -TimeoutSec 15

        $cols   = $resp.securities.columns
        $rows   = $resp.securities.data
        $isinIdx  = [array]::IndexOf($cols, "ISIN")
        $secidIdx = [array]::IndexOf($cols, "SECID")

        foreach ($row in $rows) {
            $isin  = if ($isinIdx  -ge 0) { $row[$isinIdx]  } else { $null }
            $secid = if ($secidIdx -ge 0) { $row[$secidIdx] } else { $null }
            $key   = if ($isin)  { $isin } else { $secid }

            if ($key -and -not $existingRatings.ContainsKey($key)) {
                $newRatings[$key] = $rating
                $hits++
            }
        }

        Write-Host ("[$i/{0}] {1,-30} → {2,-5}  найдено: {3}" -f $issuerMap.Count, $entry.Key, $rating, $hits)
    } catch {
        Write-Warning "Ошибка [$($entry.Key)]: $($_.Exception.Message)"
    }

    Start-Sleep -Milliseconds 150
}

# ─── Объединение и сохранение ────────────────────────────────────────────────
$merged = @{}
foreach ($k in $existingRatings.Keys) { $merged[$k] = $existingRatings[$k] }
foreach ($k in $newRatings.Keys)       { $merged[$k] = $newRatings[$k] }

$added = $merged.Count - $countBefore

$merged | ConvertTo-Json -Depth 2 | Set-Content $RatingsFile -Encoding UTF8

Write-Host ""
Write-Host "════════════════════════════════════"
Write-Host "  Было записей : $countBefore"
Write-Host "  Новых добавлено: $added"
Write-Host "  Итого записей  : $($merged.Count)"
Write-Host "════════════════════════════════════"
